// Legacy support - keep the old array for backward compatibility
export const textPrompts = themePacks.flatMap(pack => pack.prompts);

// Function to get random prompts for a round from a specific theme pack
export function getRandomPromptsFromTheme(themeId: string, count: number = 3): string[] {
  let pack = themePacks.find(p => p.id === themeId);
  
  // If theme is 'random' or not found, use all prompts
  if (!pack || themeId === 'random') {
    const allPrompts = themePacks.flatMap(p => p.prompts);
    const shuffled = [...allPrompts].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, Math.min(count, allPrompts.length));
  }
  
  const shuffled = [...pack.prompts].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, Math.min(count, pack.prompts.length));
}

// Function to get random prompts for a round (legacy support)
export function getRandomPrompts(count: number = 3): string[] {
  return getRandomPromptsFromTheme('random', count);
}

// Function to get all available theme packs
export function getThemePacks(): ThemePack[] {
  return themePacks;
}

// Function to get a specific theme pack by ID
export function getThemePack(themeId: string): ThemePack | undefined {
  return themePacks.find(pack => pack.id === themeId);
}
